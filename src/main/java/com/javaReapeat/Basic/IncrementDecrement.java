public class IncrementDecrement {
    public void IncrementDecrement() {
        int a = 5;
//      System.out.println(a += 1;
        System.out.println(a++);

        System.out.println(a++);
        System.out.println(++a);

        System.out.println(a--);
        System.out.println(--a);

    }
}
